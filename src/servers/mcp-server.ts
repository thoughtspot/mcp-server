export class MCPServer extends BaseMCPServer {
    constructor(ctx: Context, private storage?: DurableObjectStorage) {
        super(ctx, "ThoughtSpot", "1.0.0");
    }

    protected async listTools() {
        return {
            tools: [
                ...toolDefinitionsMCPServer,
                // Add data source discovery tool if available. 
                // TODO(shikhar): Add data source discovery tool to the default tool definitions once GA. 
                ...(this.isDatasourceDiscoveryAvailable() ? [{
                    name: ToolName.GetDataSourceSuggestions,
                    description: "Get data source suggestions for a query. Use this tool only if there is not datasource id provided in the context or the users query. If mulitple data sources are returned, and the confidence difference between the top two data sources is less than 0.3, ask the user to select the most relevant data source. Otherwise use the data source with the highest confidence to get the relevant questions and answers for the query.",
                    inputSchema: zodToJsonSchema(GetDataSourceSuggestionsSchema) as ToolInput,
                    annotations: {
                        title: "Get Data Source Suggestions for a Query",
                        readOnlyHint: true,
                        destructiveHint: false,
                    },
                }] : []),
            ]
        };
    }

    protected async listResources() {
        const sources = await this.getDatasources();
        return {
            resources: sources.list.map((s) => ({
                uri: `datasource:///${s.id}`,
                name: s.name,
                description: s.description,
                mimeType: "text/plain"
            }))
        };
    }

    protected async readResource(request: z.infer<typeof ReadResourceRequestSchema>) {
        const { uri } = request.params;
        const sourceId = uri.split("///").pop();
        if (!sourceId) {
            throw new McpServerError({ message: "Invalid datasource uri" }, 400);
        }
        const { map: sourceMap } = await this.getDatasources();
        const source = sourceMap.get(sourceId);
        if (!source) {
            throw new McpServerError({ message: "Datasource not found" }, 404);
        }
        return {
            contents: [{
                uri: uri,
                mimeType: "text/plain",
                text: `
                Name: ${source.name}
                ${source.description}

                The id of the datasource is ${sourceId}.

                Use ThoughtSpot's getRelevantQuestions tool to get relevant questions for a query, using the above id. And then use the getAnswer tool to get the answer for a question.
                `,
            }],
        };
    }

    protected async callTool(request: z.infer<typeof CallToolRequestSchema>) {
        const { name } = request.params;
        this.trackers.track(TrackEvent.CallTool, { toolName: name });

        let response: ToolResponse | undefined;
        switch (name) {
            case ToolName.Ping: {
                console.log("Received Ping request");
                if (this.ctx.props.accessToken && this.ctx.props.instanceUrl) {
                    response = this.createSuccessResponse("Pong", "Ping successful");
                    break;
                }
                response = this.createErrorResponse("Not authenticated", "Ping failed");
                break;
            }
            case ToolName.GetRelevantQuestions: {
                response = await this.callGetRelevantQuestions(request) as ToolResponse;
                break;
            }
            case ToolName.GetAnswer: {
                response = await this.callGetAnswer(request) as ToolResponse;
                break;
            }
            case ToolName.CreateLiveboard: {
                response = await this.callCreateLiveboard(request) as ToolResponse;
                break;
            }
            case ToolName.GetDataSourceSuggestions: {
                response = await this.callGetDataSourceSuggestions(request) as ToolResponse;
                break;
            }
            case ToolName.CreateAgentConversation: {
                response = await this.callCreateAgentConversation(request) as ToolResponse;
                break;
            }
            case ToolName.SendAgentMessage: {
                response = await this.callSendAgentMessage(request) as ToolResponse;
                break;
            }
            case ToolName.SendAgentMessageAsync: {
                response = await this.callSendAgentMessageAsync(request) as ToolResponse;
                break;
            }
            case ToolName.GetAgentMessageUpdates: {
                response = await this.callGetAgentMessageUpdates(request) as ToolResponse;
                break;
            }
            default:
                throw new Error(`Unknown tool: ${name}`);
        }

        console.log('>>> response', response);
        return response;
    }

    @WithSpan('call-get-relevant-questions')
    async callGetRelevantQuestions(request: z.infer<typeof CallToolRequestSchema>) {
        const { query, datasourceIds: sourceIds, additionalContext } = GetRelevantQuestionsSchema.parse(request.params.arguments);
        console.log("[DEBUG] Getting relevant questions for datasource: ", sourceIds);

        const relevantQuestions = await this.getThoughtSpotService().getRelevantQuestions(
            query,
            sourceIds!,
            additionalContext ?? ""
        );

        if (relevantQuestions.error) {
            console.error("Error getting relevant questions: ", relevantQuestions.error);
            
            const structuredContent = { questions: [{ question: query, datasourceId: sourceIds?.[0] ?? '' }] };
            const span = this.initSpanWithCommonAttributes();
            span?.setStatus({ code: SpanStatusCode.ERROR, message: "Relevant questions failed, sending back the query as it is" });
            span?.setAttribute("datasource_ids", sourceIds?.join(",") ?? "");
            span?.setAttribute("error", relevantQuestions.error.message);
            return {
                content: [{
                    type: "text",
                    text: JSON.stringify(structuredContent),
                }],
                structuredContent,
            };
        }

        if (relevantQuestions.questions.length === 0) {
            return this.createSuccessResponse("No relevant questions found");
        }

        return this.createStructuredContentSuccessResponse({ questions: relevantQuestions.questions }, "Relevant questions found");
    }

    @WithSpan('call-get-answer')
    async callGetAnswer(request: z.infer<typeof CallToolRequestSchema>) {
        const { question, datasourceId: sourceId } = GetAnswerSchema.parse(request.params.arguments);

        const answer = await this.getThoughtSpotService().getAnswerForQuestion(question, sourceId, false);

        if (answer.error) {
            return this.createErrorResponse(answer.error.message, `Error getting answer ${answer.error.message}`);
        }

        return this.createStructuredContentSuccessResponse({
            data: answer.data,
            question: answer.question,
            session_identifier: answer.session_identifier,
            generation_number: answer.generation_number,
            frame_url: answer.frame_url,
            fields_info: "data: The csv data as an answer to the question\n session_identifier: The session identifier for the answer, use for liveboard creation\n generation_number: The generation number for the answer, use for liveboard creation\n frame_url: A url which can be used to view the answer in an iframe in the browser\n",
        }, "Answer created successfully");
    }

    @WithSpan('call-create-liveboard')
    async callCreateLiveboard(request: z.infer<typeof CallToolRequestSchema>) {
        const { name, answers, noteTile } = CreateLiveboardSchema.parse(request.params.arguments);
        const liveboard = await this.getThoughtSpotService().fetchTMLAndCreateLiveboard(name, answers, noteTile);

        if (liveboard.error) {
            return this.createErrorResponse(liveboard.error.message, `Error creating liveboard ${liveboard.error.message}`);
        }

        const successMessage = `Liveboard created successfully, you can view it at ${liveboard.url}
                
Provide this url to the user as a link to view the liveboard in ThoughtSpot.`;

        return this.createSuccessResponse(successMessage, "Liveboard created successfully");
    }

    @WithSpan('call-get-data-source-suggestions')
    async callGetDataSourceSuggestions(request: z.infer<typeof CallToolRequestSchema>) {
        const { query } = GetDataSourceSuggestionsSchema.parse(request.params.arguments);
        const dataSources = await this.getThoughtSpotService().getDataSourceSuggestions(query);

        if (!dataSources || dataSources.length === 0) {
            return this.createErrorResponse("No data source suggestions found", "No data source suggestions found");
        }

        // Return information for all suggested data sources
        const dataSourcesInfo = dataSources.map(ds => ({
            header: ds.details ? {
                description: ds.details.description,
                displayName: ds.details.data_source_name,
                guid: ds.details.data_source_identifier,
            } : undefined,
            confidence: ds.confidence,
            llmReasoning: ds.reasoning,
        }));

        return this.createSuccessResponse(JSON.stringify(dataSourcesInfo), `${dataSources.length} data source suggestion(s) found`);
    }

    @WithSpan('call-create-agent-conversation')
    async callCreateAgentConversation(request: z.infer<typeof CallToolRequestSchema>) {
        const { metadata_context, conversation_settings } = CreateAgentConversationSchema.parse(request.params.arguments);
        const conversation = await this.getThoughtSpotService().createAgentConversation({
            metadata_context,
            conversation_settings,
        });

        return this.createStructuredContentSuccessResponse(
            { conversation_id: conversation.conversation_id },
            "Agent conversation created successfully"
        );
    }

    @WithSpan('call-send-agent-message')
    async callSendAgentMessage(request: z.infer<typeof CallToolRequestSchema>) {
        const { conversationId, messages } = SendAgentMessageSchema.parse(request.params.arguments);
        const response = await this.getThoughtSpotService().sendAgentMessage(conversationId, { messages });

        return this.createStructuredContentSuccessResponse(
            { success: true, messages: response.messages },
            "Agent message sent successfully"
        );
    }

    @WithSpan('call-send-agent-message-async')
    async callSendAgentMessageAsync(request: z.infer<typeof CallToolRequestSchema>) {
        const { conversationId, messages } = SendAgentMessageSchema.parse(request.params.arguments);
        const response = await this.getThoughtSpotService().sendAgentMessageStreaming(conversationId, { messages });

        const reader = response.body?.getReader();
        if (!reader) {
            throw new Error("Failed to get reader from response body");
        }

        setTimeout(async () => {
            const decoder = new TextDecoder();
            let buffer = "";

            while (true) {
                const { done, value } = await reader.read();
                if (done) {
                    const latestMessages = latestMessagesByConversationId.get(conversationId)?.latestMessages ?? [];
                    latestMessagesByConversationId.set(conversationId, {isDone: true, latestMessages});
                    break;
                }

                buffer += decoder.decode(value, { stream: true });
                const lines = buffer.split("\n");
                buffer = lines.pop() || "";

                for (const line of lines) {
                    if (!line.startsWith("data: ")) continue;

                    try {
                        const data = JSON.parse(line.slice(6));
                        data.forEach((item: any) => {
                            if (item.type === 'text-chunk') {
                                const latestMessages = latestMessagesByConversationId.get(conversationId)?.latestMessages ?? [];
                                latestMessages.push({
                                    type: 'text-chunk',
                                    text: item.content,
                                });
                                latestMessagesByConversationId.set(conversationId, {isDone: false, latestMessages});
                            } else if (item.type === 'answer') {
                                const latestMessages = latestMessagesByConversationId.get(conversationId)?.latestMessages ?? [];
                                latestMessages.push({
                                    type: 'answer',
                                    answerTitle: item.metadata.title,
                                    answerQuery: item.metadata.sage_query,
                                });
                                latestMessagesByConversationId.set(conversationId, {isDone: false, latestMessages});
                            } else {
                                console.log('>>> unknown item in event stream', item);
                            }
                        });
                    } catch {
                        console.log('>>> error parsing line', line);
                    }
                }
            }
        });

        return this.createStructuredContentSuccessResponse(
            { success: true },
            "Agent message sent successfully"
        );
    }

    @WithSpan('call-get-agent-message-updates')
    async callGetAgentMessageUpdates(request: z.infer<typeof CallToolRequestSchema>) {
        const conversationId = request.params?.arguments?.conversationId as string;
        const latestStatus = latestMessagesByConversationId.get(conversationId);
        if (!latestStatus) {
            console.log('>>> no latest status for conversationId', conversationId);
        }
        latestMessagesByConversationId.delete(conversationId);

        return this.createStructuredContentSuccessResponse(
            { messages: latestStatus?.latestMessages ?? [], done: latestStatus?.isDone ?? false },
            "Agent message updates retrieved successfully"
        );
    }

    private _sources: {
        list: DataSource[];
        map: Map<string, DataSource>;
    } | null = null;

    @WithSpan('get-datasources')
    async getDatasources() {
        if (this._sources) {
            return this._sources;
        }

        const sources = await this.getThoughtSpotService().getDataSources();
        this._sources = {
            list: sources,
            map: new Map(sources.map(s => [s.id, s])),
        }
        return this._sources;
    }


}
