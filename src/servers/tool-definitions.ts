export const ConversationUpdateSchema = z.object({
	type: z
		.enum(["text", "text_chunk", "answer"])
		.describe(
			"The type of update: `text` or `text_chunk` for a natural language message from the Analytics Agent, or `answer` for a data visualization with query results. Determines which other fields are populated.",
		),
	is_thinking: z
		.boolean()
		.describe(
			"Whether this update is part of the Analytics Agent's thinking process. You can use this to separate thinking updates from final result updates. You can use the thinking updates to show intermediate status or progress information to the user.",
		),
	text: z
		.string()
		.optional()
		.describe(
			"The text content of the message. Only present when `type` is `text` or `text_chunk`. If `type` is `text_chunk`, concatenate the chunks of text together in the order received to form the complete message.",
		),
	answer_id: z
		.string()
		.optional()
		.describe(
			"A unique identifier for the answer. Only present when `type` is `answer`. You can use this field to identify this answer when using the `create_dashboard` tool.",
		),
	answer_title: z
		.string()
		.optional()
		.describe(
			"A human-readable title describing what the answer shows. Only present when `type` is `answer`.",
		),
	answer_query: z
		.string()
		.optional()
		.describe(
			"The search query which the Analytics Agent used to generate the answer. Only present when `type` is `answer`. You can use this to explain to the user what data was queried, or to diagnose unexpected results.",
		),
	iframe_url: z
		.string()
		.optional()
		.describe(
			"An embeddable URL for displaying the answer as an interactive visualization. Only present when `type` is `answer`. You can use this to render a live chart or table if your environment supports iframes.",
		),
});

export const GetSessionUpdatesOutputSchema = z.object({
	session_updates: z
		.array(ConversationUpdateSchema)
		.describe(
			"List of incremental new updates from the analytical session. Updates returned in a previous response will not be sent again. This may be an empty list if the Analytics Agent is still thinking. You can instead use the `is_done` flag to know whether the Agent response is complete.",
		),
	is_done: z
		.boolean()
		.describe(
			"Whether the Analytics Agent has finished responding. If `is_done` is false, you can make another `get_session_updates` call to get the next set of updates.",
		),
});

export const CreateLiveboardSchema = z.object({
	name: z.string().describe("The name of the liveboard to create"),
	answers: z
		.array(
			z.object({
				question: z.string(),
				session_identifier: z.string(),
				generation_number: z.number(),
			}),
		)
		.describe(
			"The answers to create the liveboard from, these are the answers generated by the getAnswer tool.",
		),
	noteTile: z.string().describe(`
        Create a summary of the answers to the queries asked by the user along with all the analysis done on the data.
        The output format is an html note tile which can have custom styles and formatting. Use emojis, styling and icons to make it more visually appealing.
        Follow these MANDATORY rules for styling:
        - Put the whole note tile in a single line and no line breaks. Also there should be no unnecessary white spaces.
        - The html output in the note tile can have white spaces. Use <br> or <tab> if needed to use white spaces in the note tile.
        - Use padding, margin and line height to create space between the elements in the note tile and make it more readable.

        Use this as an example(ignore the line breaks and white spaces):

        <h2 class=\"theme-module__editor-h2\" dir=\"ltr\" style=\"text-align: center;\">
	        <span style=\"white-space: pre-wrap;\">\"Heading of note\"</span>
        </h2>
        <p class=\"theme-module__editor-paragraph\" dir=\"ltr\">
            <span style=\"white-space: pre-wrap;\">
                Comprehensive summary of analysis done and answers to queries asked by user. Use colors and styles in the html to make it more readable and visually appealing.
                Also add the date and time of the analysis to the note tile in the header of the note tile. The format should be like this: Generated on <date> <time>
            </span>
        </p>
        <div class=\"pinboard-note-tile-module__noteTileBg editor-module__bgNode\"></div>
        `),
});

